package Model;

public class StuScore {
	public StuScore(String stuid, String sname, String courseid, String cname, int score) {
		super();
		this.stuid = stuid;
		this.sname = sname;
		this.courseid = courseid;
		this.cname = cname;
		this.score = score;
	}
	public String stuid;
	public String sname;
	public String courseid;
	public String cname;
	public int score;
}
